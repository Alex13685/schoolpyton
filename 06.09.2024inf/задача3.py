import math

def незайнята_площа_кола(r):
    площа_кола = math.pi * r**2
    площа_трикутника = (3 * math.sqrt(3) * r**2) / 4
    незайнята_площа = площа_кола - площа_трикутника
    return незайнята_площа

# Приклад виклику функції
r = 5  # радіус кола
результат = незайнята_площа_кола(r)
print(f"Площа кола, яку не займає трикутник: {результат:.2f}")
