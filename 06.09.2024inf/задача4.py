import math

def незайнята_площа_трикутника(a):
    площа_трикутника = (math.sqrt(3) / 4) * a**2
    площа_кола = (math.pi * a**2) / 12
    незайнята_площа = площа_трикутника - площа_кола
    return незайнята_площа

# Приклад виклику функції
a = 6  # сторона трикутника
результат = незайнята_площа_трикутника(a)
print(f"Площа трикутника, яку не займає коло: {результат:.2f}")
